package main;

public class Recuerdame {
	
	 private String x,y,color,textor;

	    public Recuerdame(String x,String y,String color,String textor){

	    	this.x=x;
	    	this.y=y;
	    	this.color=color;
	    	this.textor=textor;
	    	
	        

	    }

		public String getY() {
			return y;
		}

		public void setY(String y) {
			this.y = y;
		}

		public String getX() {
			return x;
		}

		public void setX(String x) {
			this.x = x;
		}

		public String getColor() {
			return color;
		}

		public void setColor(String color) {
			this.color = color;
		}

		public String getTextor() {
			return textor;
		}

		public void setTextor(String textor) {
			this.textor = textor;
		}


}
